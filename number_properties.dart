main() {
  var num = 25;
  print("bitlength");
  print(num.bitLength);

  print("isEven");
  print(num.isEven);

  print("isOdd");
  print(num.isOdd);

  print("sign");
  print(num.sign);

  print("hashCode");
  print(num.hashCode);

  print("isFinite");
  print(num.isFinite);

  print("compareTO()");
  print(27.compareTo(26.9.ceil()));

  print("ceil()");
  print(26.9.ceil());

  print("isInfinite");
  print(num.isInfinite);

  print("isNan");
  print(num.isNaN);

  print("isNegative");
  print(num.isNegative);

  print("runTime");
  print(num.runtimeType);

  print("abs()");
  print(num.abs());

  print("floor()");
  print(4.6.floor());

  print("remainder()");
  print(num.remainder(4));

  print("round()");
  print(88.0.round());

