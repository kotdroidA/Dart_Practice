main() {
  var num1 = 10;
  var num2 = 5;

  // addition operator
  print("sum = ${num1 + num2}");

  // subtraction operator
  print("difference = ${num1 - num2}");

  //multipliction operator
  print("product = ${num1 * num2}");

  //divide operator
  print("quo = ${num1 / num2}");

  //modulus operator
  print("modulus = ${num1 % num2}");

  //Division operator
  print("divi = ${num1 ~/ num2}");

  //num1 = num1 + num2;
  num1 += num2;
  print(num1);
}
